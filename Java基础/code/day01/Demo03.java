// 变量的基本使用
public class Demo03{
	public static void main(String[] args){
		
		//1. 定义一个整数类型变量
		int a = 18;
		System.out.println(a);
		
		a = 16;		// 改变变量a的值
		System.out.println(a);
		
		//2. 需求：定义小数类型变量并打印
		double b = 3.14;
		System.out.println(b);
		
		//3. 需求：定义字符类型变量并打印
		char c = 'a';
		System.out.println(c);
		
		//4. 需求：定义布尔类型变量并打印
		boolean d = true;
		System.out.println(d);
	}
}