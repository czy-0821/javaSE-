public class Demo02 {
    public static void main(String[] args) {
        int[] arr = new int[2];
        arr[0] = 11;
        arr[1] = 22;
        System.out.println(arr[0]);
        System.out.println(arr[1]);

        double[] brr = new double[3];
        brr[0] = 3.14;
        brr[1] = 5.21;
        System.out.println(brr[0]);
        System.out.println(brr[1]);
        System.out.println(brr[2]);
    }
}
